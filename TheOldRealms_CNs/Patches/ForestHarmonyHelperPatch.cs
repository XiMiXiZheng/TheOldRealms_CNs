        [HarmonyTranspiler]
        [HarmonyPatch(typeof(ForestHarmonyHelper), "GetForestSymbolText")]
        public static IEnumerable<CodeInstruction> TranspilerGetForestSymbolText(IEnumerable<CodeInstruction> instructions, ILGenerator generator)
        {
            var found = false;
            foreach (var instruction in instructions)
            {
                if (instruction.opcode == OpCodes.Ldstr)
                {
                    var stringOperand = instruction.operand.ToString();
                    switch (stringOperand)
                    {
                        case "Current active symbol: ":
                            yield return new CodeInstruction(OpCodes.Ldstr, "{=U9JmGIHC}Current active symbol: ");
                            yield return new CodeInstruction(OpCodes.Ldnull);
                            yield return new CodeInstruction(OpCodes.Newobj, AccessTools.Constructor(typeof(TextObject), new Type[] { typeof(string), typeof(Dictionary<string, object>) }));
                            yield return new CodeInstruction(OpCodes.Callvirt, AccessTools.Method(typeof(Object), nameof(Object.ToString)));
                            found = true;
                            break;
                        default:
                            yield return instruction;
                            break;
                    }
                }
                else
                {
                    yield return instruction;
                }
            }
            if (found is false)
                throw new ArgumentException("Cannot find ldstr 'Current active symbol: ' in ForestHarmonyHelper.GetForestSymbolText");
        }

